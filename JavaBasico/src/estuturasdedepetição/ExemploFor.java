package estuturasdedepetição;
public class ExemploFor {

    public static void main(String[] args) {

        // PARA (variavel; condição booleana; incremento/atualização)
        for(int carneirinhos = 1; carneirinhos <= 10; carneirinhos++) {
            
            //comando enquanto a condição for verdadeira
            System.out.println("Contando " + carneirinhos);
        }

        //comando quando a condição se tornar falsa
        System.out.println("A criança dormiu");
    }
        
}
